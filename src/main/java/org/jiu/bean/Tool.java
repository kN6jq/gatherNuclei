package org.jiu.bean;

public class Tool {
    private String templatePath;
    private String fofaUrl;
    private String fofaEmail;
    private String fofaKey;
    private String hunterUrl;
    private String hunterKey;

    public Tool() {
    }

    public String getTemplatePath() {
        return templatePath;
    }

    public void setTemplatePath(String templatePath) {
        this.templatePath = templatePath;
    }

    public String getFofaUrl() {
        return fofaUrl;
    }

    public void setFofaUrl(String fofaUrl) {
        this.fofaUrl = fofaUrl;
    }

    public String getFofaEmail() {
        return fofaEmail;
    }

    public void setFofaEmail(String fofaEmail) {
        this.fofaEmail = fofaEmail;
    }

    public String getFofaKey() {
        return fofaKey;
    }

    public void setFofaKey(String fofaKey) {
        this.fofaKey = fofaKey;
    }

    public String getHunterUrl() {
        return hunterUrl;
    }

    public void setHunterUrl(String hunterUrl) {
        this.hunterUrl = hunterUrl;
    }

    public String getHunterKey() {
        return hunterKey;
    }

    public void setHunterKey(String hunterKey) {
        this.hunterKey = hunterKey;
    }
}
